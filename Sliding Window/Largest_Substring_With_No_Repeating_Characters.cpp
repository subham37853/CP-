int longestUniqueSubstr(string s) {
	int ans = 0;
	vector<int> m(256, -1);
	int j = 0;
	for (int i = 0; i < s.length(); i++) {
		j = max(j, m[s[i]] + 1);
		ans = max(ans, i - j + 1);
		m[s[i]] = i;
	}
	return ans;
}

